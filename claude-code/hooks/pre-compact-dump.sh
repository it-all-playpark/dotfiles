#!/usr/bin/env bash
# PreCompact hook: /compact または auto-compact 直前に session 状態をファイルへ退避
#
# 目的:
#   Claude 自身の要約に委ねず、deterministic に session 状態を claudedocs/session-*.md
#   へダンプする。次 session の SessionStart hook で最新ファイルを cat することで、
#   compaction 時の情報欠落 (= "知らないことを知らない" ギャップ) を防ぐ。
#
# stdin JSON (Claude Code PreCompact hook):
#   {
#     "session_id": "...",
#     "transcript_path": "...",
#     "cwd": "/absolute/path",
#     "hook_event_name": "PreCompact",
#     "trigger": "manual" | "auto",
#     "custom_instructions": "..."
#   }
#
# 出力: <project-root>/claudedocs/session-YYYYMMDD-HHMMSS.md
# ローテーション: 同ディレクトリ内の session-*.md を 14 日で削除

set -euo pipefail

# --- stdin 読み取り (JSON 前提、欠落時は fallback) -------------------------
INPUT=""
if [ ! -t 0 ]; then
  INPUT=$(cat || true)
fi

json_get() {
  local key="$1"
  local default="${2:-}"
  if [ -z "$INPUT" ]; then
    printf '%s' "$default"
    return
  fi
  if command -v jq >/dev/null 2>&1; then
    local v
    v=$(printf '%s' "$INPUT" | jq -r ".$key // empty" 2>/dev/null || true)
    if [ -n "$v" ]; then
      printf '%s' "$v"
    else
      printf '%s' "$default"
    fi
  else
    printf '%s' "$default"
  fi
}

SESSION_ID=$(json_get session_id "unknown")
TRIGGER=$(json_get trigger "manual")
CWD=$(json_get cwd "$PWD")
TRANSCRIPT_PATH=$(json_get transcript_path "")

# --- project root の決定 --------------------------------------------------
cd "$CWD" 2>/dev/null || cd "$PWD"

if git rev-parse --show-toplevel >/dev/null 2>&1; then
  PROJECT_ROOT=$(git rev-parse --show-toplevel)
else
  PROJECT_ROOT="$PWD"
fi

CLAUDEDOCS_DIR="$PROJECT_ROOT/claudedocs"
mkdir -p "$CLAUDEDOCS_DIR"

TIMESTAMP=$(date +%Y%m%d-%H%M%S)
DUMP_FILE="$CLAUDEDOCS_DIR/session-$TIMESTAMP.md"

# --- git 情報収集 ---------------------------------------------------------
BRANCH="(none)"
WORKTREE="(none)"
STATUS_SHORT=""
RECENT_COMMITS=""
DIFF_STAT=""

if git rev-parse --is-inside-work-tree >/dev/null 2>&1; then
  BRANCH=$(git rev-parse --abbrev-ref HEAD 2>/dev/null || echo "(detached)")
  WORKTREE=$(git rev-parse --show-toplevel 2>/dev/null || echo "$PROJECT_ROOT")
  STATUS_SHORT=$(git status --short 2>/dev/null || true)
  RECENT_COMMITS=$(git log --oneline -n 10 2>/dev/null || true)
  DIFF_STAT=$(git diff --stat HEAD 2>/dev/null || true)
fi

# --- decisions-*.md 探索 (プロジェクト固有の決定事項) -----------------------
DECISIONS_LIST=""
if [ -d "$CLAUDEDOCS_DIR" ]; then
  DECISIONS_LIST=$(find "$CLAUDEDOCS_DIR" -maxdepth 1 -name 'decisions-*.md' -type f 2>/dev/null | sort || true)
fi

# --- 直近の tool_use サマリ (permission-journal などから末尾を拾う) ---------
RECENT_TOOLS=""
PERM_LOG="$HOME/.claude/logs/permission-requests.jsonl"
if [ -f "$PERM_LOG" ] && command -v jq >/dev/null 2>&1; then
  RECENT_TOOLS=$(tail -n 20 "$PERM_LOG" 2>/dev/null |
    jq -r 'select(.session == "'"$SESSION_ID"'") | "- [" + .ts + "] " + .tool + ": " + (.detail // "")' 2>/dev/null ||
    true)
  # session_id でマッチしない場合は末尾 10 件を無条件に拾う
  if [ -z "$RECENT_TOOLS" ]; then
    RECENT_TOOLS=$(tail -n 10 "$PERM_LOG" 2>/dev/null |
      jq -r '"- [" + .ts + "] " + .tool + ": " + (.detail // "")' 2>/dev/null ||
      true)
  fi
fi

# --- dump ファイル書き込み -------------------------------------------------
{
  echo "# Session Dump (pre-compact)"
  echo ""
  echo "- **timestamp**: $(date -u +%Y-%m-%dT%H:%M:%SZ)"
  echo "- **session_id**: $SESSION_ID"
  echo "- **trigger**: $TRIGGER"
  echo "- **cwd**: $CWD"
  echo "- **project_root**: $PROJECT_ROOT"
  echo "- **worktree**: $WORKTREE"
  echo "- **branch**: $BRANCH"
  if [ -n "$TRANSCRIPT_PATH" ]; then
    echo "- **transcript**: $TRANSCRIPT_PATH"
  fi
  echo ""

  echo "## Git Status (short)"
  echo ""
  if [ -n "$STATUS_SHORT" ]; then
    echo '```'
    printf '%s\n' "$STATUS_SHORT"
    echo '```'
  else
    echo "_clean_"
  fi
  echo ""

  echo "## Git Diff Stat (HEAD)"
  echo ""
  if [ -n "$DIFF_STAT" ]; then
    echo '```'
    printf '%s\n' "$DIFF_STAT"
    echo '```'
  else
    echo "_no uncommitted changes_"
  fi
  echo ""

  echo "## Recent Commits"
  echo ""
  if [ -n "$RECENT_COMMITS" ]; then
    echo '```'
    printf '%s\n' "$RECENT_COMMITS"
    echo '```'
  else
    echo "_n/a_"
  fi
  echo ""

  echo "## Recent Tool Use"
  echo ""
  if [ -n "$RECENT_TOOLS" ]; then
    printf '%s\n' "$RECENT_TOOLS"
  else
    echo "_no tool use log available_"
  fi
  echo ""

  echo "## Decisions (claudedocs/decisions-*.md)"
  echo ""
  if [ -n "$DECISIONS_LIST" ]; then
    while IFS= read -r f; do
      [ -z "$f" ] && continue
      echo "### $(basename "$f")"
      echo ""
      echo '```markdown'
      # 長すぎる decisions ファイルは先頭 200 行に制限
      head -n 200 "$f"
      echo '```'
      echo ""
    done <<<"$DECISIONS_LIST"
  else
    echo "_no decisions files_"
  fi
  echo ""

  echo "---"
  echo ""
  echo "_Generated by pre-compact-dump.sh_"
} >"$DUMP_FILE"

# --- 14 日以上前の dump を削除 (rotation) ----------------------------------
find "$CLAUDEDOCS_DIR" -maxdepth 1 -name 'session-*.md' -type f -mtime +14 -delete 2>/dev/null || true

# PreCompact hook は stdout を context に追加可能だが、ここでは静かに成功する
# (SessionStart hook 側で最新の dump を読み込む)
echo "[pre-compact-dump] wrote $DUMP_FILE" >&2
exit 0
