#!/usr/bin/env bash
set -euo pipefail

# generate-worktreeinclude.sh
# Pre-hook for "git worktree add": auto-generate .worktreeinclude so that
# .env files and Claude Code local config are copied into new worktrees.

GIT_ROOT=$(git rev-parse --show-toplevel 2>/dev/null) || {
  echo "[generate-worktreeinclude] Not inside a git repository, skipping." >&2
  exit 0
}

WORKTREEINCLUDE="$GIT_ROOT/.worktreeinclude"

# If .worktreeinclude already exists, do nothing
if [[ -f $WORKTREEINCLUDE ]]; then
  echo "[generate-worktreeinclude] .worktreeinclude already exists, skipping." >&2
  exit 0
fi

# Scan for .env* files, excluding common non-source directories
ENV_FILES=$(find "$GIT_ROOT" \
  -name '.env*' \
  -not -path '*/node_modules/*' \
  -not -path '*/.git/*' \
  -not -path '*/*-worktrees/*' \
  -not -path '*/vendor/*' \
  -not -path '*/dist/*' \
  -not -path '*/build/*' \
  2>/dev/null || true)

# Extract unique filenames (basenames) from found .env* files
PATTERNS=()
if [[ -n $ENV_FILES ]]; then
  while IFS= read -r fname; do
    PATTERNS+=("$fname")
  done < <(echo "$ENV_FILES" | xargs -I{} basename {} | sort -u)
fi

# Build .worktreeinclude content
{
  echo "# Auto-generated by generate-worktreeinclude.sh"
  echo "# Patterns for files to copy into new worktrees"
  echo ""

  if [[ ${#PATTERNS[@]} -gt 0 ]]; then
    echo "# .env files"
    for pattern in "${PATTERNS[@]}"; do
      echo "$pattern"
      echo "**/$pattern"
    done
    echo ""
  fi

  echo "# Claude Code local config"
  echo ".claude/settings.local.json"
} >"$WORKTREEINCLUDE"

echo "[generate-worktreeinclude] Created $WORKTREEINCLUDE with ${#PATTERNS[@]} env pattern(s)." >&2
